package com.smart.rct.common.models;

public class SiteCarriers {
	private String carriersId;
	private String likeforlike;
	private String incremental;
	private String likeforlikeCheckBox;
	private String incrementalCheckBox;
	
	
	public String getCarriersId() {
		return carriersId;
	}
	public void setCarriersId(String carriersId) {
		this.carriersId = carriersId;
	}
	public String getLikeforlike() {
		return likeforlike;
	}
	public void setLikeforlike(String likeforlike) {
		this.likeforlike = likeforlike;
	}
	public String getIncremental() {
		return incremental;
	}
	public void setIncremental(String incremental) {
		this.incremental = incremental;
	}
	public String getLikeforlikeCheckBox() {
		return likeforlikeCheckBox;
	}
	public void setLikeforlikeCheckBox(String likeforlikeCheckBox) {
		this.likeforlikeCheckBox = likeforlikeCheckBox;
	}
	public String getIncrementalCheckBox() {
		return incrementalCheckBox;
	}
	public void setIncrementalCheckBox(String incrementalCheckBox) {
		this.incrementalCheckBox = incrementalCheckBox;
	}
	
	

}
